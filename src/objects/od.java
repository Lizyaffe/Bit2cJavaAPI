package objects;

public class od {
	private double d;
    private boolean t;
    private int s;
    private double a;
    private double p;
    private double p1;
    private double id;
    private int aa;
    
    public od(double d, boolean t, int s, double a, double p, double p1, double id, int aa){
    	this.d = d;
    	this.t = t;
    	this.s = s;
    	this.a = a;
    	this.p = p;
    	this.p1 = p1;
    	this.id = id;
    	this.aa = aa;
    }
    
	public double getD() {
		return d;
	}
	
	public boolean isT() {
		return t;
	}
	
	public int getS() {
		return s;
	}
	
	public double getA() {
		return a;
	}
	
	public double getP() {
		return p;
	}
	
	public double getP1() {
		return p1;
	}
	
	public double getId() {
		return id;
	}
	
	public int getAa() {
		return aa;
	}
}
